SELECT AVG(SAL) AS Average_Salary FROM EMP;

SELECT COUNT(*) AS Total_Employees FROM EMP;

SELECT COUNT(DISTINCT EMP_NO) AS Distinct_Employees FROM EMP;

SELECT JOB, SUM(SAL) AS Total_Salary_by_Job FROM EMP
GROUP BY JOB;

SELECT *
FROM EMP
WHERE SAL = (SELECT MAX(SAL) FROM EMP);

SELECT *
FROM EMP
WHERE DEPT_NO = 10
ORDER BY SAL DESC
LIMIT 1;

SELECT *
FROM EMP
WHERE SAL = (
    SELECT (MAX(SAL) + MIN(SAL)) / 2
    FROM EMP
);

SELECT EMP_NO, EMP_NAME, HIRE_DATE
FROM EMP
WHERE HIRE_DATE IN (
    SELECT HIRE_DATE
    FROM EMP
    GROUP BY HIRE_DATE
    HAVING COUNT(*) > 1 
)
ORDER BY HIRE_DATE;

